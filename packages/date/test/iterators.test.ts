import { expect, test } from "bun:test";
import {
	FMT_yyyyMMdd,
	YEAR,
	dateTime,
	days,
	defFormat,
	hours,
	months,
	years,
} from "../src/index.js";

test("hours", () => {
	const fmt = defFormat(["dd", " ", "h", ":", "mm", " ", "A"]);
	expect(
		[
			...hours(
				Date.UTC(2019, 11, 30, 15, 1),
				Date.UTC(2019, 11, 31, 15, 1)
			),
		].map((x) => fmt(x, true))
	).toEqual([
		"30 4:00 PM",
		"30 5:00 PM",
		"30 6:00 PM",
		"30 7:00 PM",
		"30 8:00 PM",
		"30 9:00 PM",
		"30 10:00 PM",
		"30 11:00 PM",
		"31 12:00 AM",
		"31 1:00 AM",
		"31 2:00 AM",
		"31 3:00 AM",
		"31 4:00 AM",
		"31 5:00 AM",
		"31 6:00 AM",
		"31 7:00 AM",
		"31 8:00 AM",
		"31 9:00 AM",
		"31 10:00 AM",
		"31 11:00 AM",
		"31 12:00 PM",
		"31 1:00 PM",
		"31 2:00 PM",
		"31 3:00 PM",
	]);
});

