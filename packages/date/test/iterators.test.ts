test("years", () => {
	expect(
		[...years(Date.UTC(1996, 10, 30, 15), Date.UTC(2005, 10, 1, 1))].map(
			(x) => FMT_yyyyMMdd(x)
		)
	).toEqual([
		"1997-01-01",
		"1998-01-01",
		"1999-01-01",
		"2000-01-01",
		"2001-01-01",
		"2002-01-01",
		"2003-01-01",
		"2004-01-01",
		"2005-01-01",
	]);
});

test("arg coercion", () => {
	expect(
		[...years(dateTime(0), dateTime(YEAR))].map((x) => FMT_yyyyMMdd(x))
	).toEqual(["1970-01-01", "1971-01-01"]);
	expect(
		[...years(new Date(0), new Date(YEAR))].map((x) => FMT_yyyyMMdd(x))
	).toEqual(["1970-01-01", "1971-01-01"]);
});
